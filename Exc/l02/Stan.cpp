#include <iostream>
#include <string.h>
#include "Stan.h"

	Stan::Stan()
		: _stan ( 0 )
	{}
	/*************************************************************************************/
	Stan::~Stan(){
		delete [] _stan;
	}
	/*************************************************************************************/
	Stan::Stan( const char* string ){
		_stan = new char [ strlen( string ) + 1 ];
		strcpy( _stan, string );
	}
	/*************************************************************************************/
	void Stan::operator()(){
		std::cout << _stan << std::endl;
	}
	/*************************************************************************************/
